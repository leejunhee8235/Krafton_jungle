# # 링크드리스트 - 에디터 (백준 실버2)
# # 문제 링크: https://www.acmicpc.net/problem/1406
# 문제
# 한 줄로 된 간단한 에디터를 구현하려고 한다. 
# 이 편집기는 영어 소문자만을 기록할 수 있는 편집기로, 최대 600,000글자까지 입력할 수 있다.

# 이 편집기에는 '커서'라는 것이 있는데, 커서는 문장의 맨 앞(첫 번째 문자의 왼쪽), 
# 문장의 맨 뒤(마지막 문자의 오른쪽), 또는 문장 중간 임의의 곳(모든 연속된 두 문자 사이)에 위치할 수 있다. 
# 즉 길이가 L인 문자열이 현재 편집기에 입력되어 있으면, 커서가 위치할 수 있는 곳은 L+1가지 경우가 있다.

# 이 편집기가 지원하는 명령어는 다음과 같다.

# L	커서를 왼쪽으로 한 칸 옮김 (커서가 문장의 맨 앞이면 무시됨)
# D	커서를 오른쪽으로 한 칸 옮김 (커서가 문장의 맨 뒤이면 무시됨)
# B	커서 왼쪽에 있는 문자를 삭제함 (커서가 문장의 맨 앞이면 무시됨)
# 삭제로 인해 커서는 한 칸 왼쪽으로 이동한 것처럼 나타나지만, 실제로 커서의 오른쪽에 있던 문자는 그대로임
# P $	$라는 문자를 커서 왼쪽에 추가함
# 초기에 편집기에 입력되어 있는 문자열이 주어지고, 그 이후 입력한 명령어가 차례로 주어졌을 때, 
# 모든 명령어를 수행하고 난 후 편집기에 입력되어 있는 문자열을 구하는 프로그램을 작성하시오. 
# 단, 명령어가 수행되기 전에 커서는 문장의 맨 뒤에 위치하고 있다고 한다.

# 입력
# 첫째 줄에는 초기에 편집기에 입력되어 있는 문자열이 주어진다. 이 문자열은 길이가 N이고, 
# 영어 소문자로만 이루어져 있으며, 길이는 100,000을 넘지 않는다. 
# 둘째 줄에는 입력할 명령어의 개수를 나타내는 정수 M(1 ≤ M ≤ 500,000)이 주어진다. 
# 셋째 줄부터 M개의 줄에 걸쳐 입력할 명령어가 순서대로 주어진다. 
# 명령어는 위의 네 가지 중 하나의 형태로만 주어진다.

# 출력
# 첫째 줄에 모든 명령어를 수행하고 난 후 편집기에 입력되어 있는 문자열을 출력한다.

import sys  # 빠른 입력을 받기 위해 sys 모듈 사용

# 처음 문자열 입력
# strip()으로 줄바꿈 제거
# list()로 한 글자씩 나눠서 리스트로 저장
# 처음 커서는 맨 뒤에 있으므로 초기 문자열 전체가 커서 왼쪽에 있음
left = list(sys.stdin.readline().strip())

# 커서 오른쪽에 있는 문자들을 저장할 리스트
# 처음에는 커서가 맨 뒤이므로 오른쪽은 비어 있음
right = []

# 명령어 개수 입력
m = int(sys.stdin.readline())

# 명령어 개수만큼 반복
for _ in range(m):
    # 명령어 입력
    # 예: "L" -> ['L']
    # 예: "P x" -> ['P', 'x']
    command = sys.stdin.readline().split()

    # 명령어가 L이면: 커서를 왼쪽으로 한 칸 이동
    if command[0] == 'L':
        # 커서 왼쪽에 문자가 있을 때만 이동 가능
        if left:
            # 왼쪽의 맨 끝 문자 1개를 꺼내서
            # 오른쪽 리스트에 넣음
            right.append(left.pop())

    # 명령어가 D이면: 커서를 오른쪽으로 한 칸 이동
    elif command[0] == 'D':
        # 커서 오른쪽에 문자가 있을 때만 이동 가능
        if right:
            # 오른쪽의 맨 끝 문자 1개를 꺼내서
            # 왼쪽 리스트에 넣음
            left.append(right.pop())

    # 명령어가 B이면: 커서 왼쪽 문자 삭제
    elif command[0] == 'B':
        # 커서 왼쪽에 문자가 있을 때만 삭제 가능
        if left:
            # 왼쪽의 맨 끝 문자 삭제
            left.pop()

    # 명령어가 P이면: 커서 왼쪽에 문자 추가
    elif command[0] == 'P':
        # command[1]에 추가할 문자가 들어있음
        # 예: "P x" 라면 command[1]은 'x'
        left.append(command[1])

# 최종 출력
# left는 원래 순서대로 저장되어 있음
# right는 반대로 쌓여 있으므로 뒤집어서(right[::-1]) 붙여야 함
# join으로 문자 리스트를 하나의 문자열로 합쳐 출력
print(''.join(left + right[::-1]))