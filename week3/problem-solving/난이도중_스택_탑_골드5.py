import sys
input = sys.stdin.readline

N = int(input())
tops = list(map(int, input().split()))

stack = []   # (탑 번호, 높이) 저장
answer = []

for i in range(N):
    height = tops[i]

    # 현재 탑보다 낮은 탑들은 의미 없으니 제거
    while stack and stack[-1][1] < height:
        stack.pop()

    # 스택이 비어 있으면 왼쪽에 나보다 큰 탑이 없음
    if not stack:
        answer.append(0)
    else:
        answer.append(stack[-1][0])

    # 현재 탑을 스택에 넣기
    stack.append((i + 1, height))
# (1,6) -> 제거 -> not stack -> (2,9)-> [0,0,2]-> (2,9)(3,5) ->(3,5) 제거 -> [0,0,2,2]->(2,9)(4,7)->4 ->[0,0,2,2,4]
print(*answer)




