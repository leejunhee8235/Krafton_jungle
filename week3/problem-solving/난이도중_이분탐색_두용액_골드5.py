# # 이분탐색 - 두 용액 (백준 골드5)
# # 문제 링크: https://www.acmicpc.net/problem/2470
# 문제
# KOI 부설 과학연구소에서는 많은 종류의 산성 용액과 알칼리성 용액을 보유하고 있다. 
# 각 용액에는 그 용액의 특성을 나타내는 하나의 정수가 주어져있다. 
# 산성 용액의 특성값은 1부터 1,000,000,000까지의 양의 정수로 나타내고, 
# 알칼리성 용액의 특성값은 -1부터 -1,000,000,000까지의 음의 정수로 나타낸다.

# 같은 양의 두 용액을 혼합한 용액의 특성값은 혼합에 사용된 각 용액의 특성값의 합으로 정의한다. 
# 이 연구소에서는 같은 양의 두 용액을 혼합하여 특성값이 0에 가장 가까운 용액을 만들려고 한다.

# 예를 들어, 주어진 용액들의 특성값이 [-2, 4, -99, -1, 98]인 경우에는 
# 특성값이 -99인 용액과 특성값이 98인 용액을 혼합하면 특성값이 -1인 용액을 만들 수 있고, 
# 이 용액이 특성값이 0에 가장 가까운 용액이다. 참고로, 두 종류의 알칼리성 용액만으로나 
# 혹은 두 종류의 산성 용액만으로 특성값이 0에 가장 가까운 혼합 용액을 만드는 경우도 존재할 수 있다.

# 산성 용액과 알칼리성 용액의 특성값이 주어졌을 때, 
# 이 중 두 개의 서로 다른 용액을 혼합하여 특성값이 0에 가장 가까운 용액을 만들어내는 
# 두 용액을 찾는 프로그램을 작성하시오.

# 입력
# 첫째 줄에는 전체 용액의 수 N이 입력된다. 
# N은 2 이상 100,000 이하이다. 둘째 줄에는 용액의 특성값을 나타내는 N개의 정수가 빈칸을 사이에 두고 주어진다. 
# 이 수들은 모두 -1,000,000,000 이상 1,000,000,000 이하이다. N개의 용액들의 특성값은 모두 다르고, 
# 산성 용액만으로나 알칼리성 용액만으로 입력이 주어지는 경우도 있을 수 있다.

# 출력
# 첫째 줄에 특성값이 0에 가장 가까운 용액을 만들어내는 두 용액의 특성값을 출력한다. 
# 출력해야 하는 두 용액은 특성값의 오름차순으로 출력한다. 
# 특성값이 0에 가장 가까운 용액을 만들어내는 경우가 두 개 이상일 경우에는 그 중 아무것이나 하나를 출력한다.

import sys
input = sys.stdin.readline

n = int(input())
# 전체 용액의 개수 입력

arr = list(map(int, input().split()))
# 용액들의 특성값을 리스트로 입력받음
# 예: -2 4 -99 -1 98

arr.sort()
# 투포인터를 쓰려면 정렬이 꼭 필요함
# 예: [-2, 4, -99, -1, 98] -> [-99, -2, -1, 4, 98]

left = 0
# 왼쪽 포인터: 맨 앞 인덱스부터 시작

right = n - 1
# 오른쪽 포인터: 맨 뒤 인덱스부터 시작

best = float('inf')
# 현재까지 찾은 "0에 가장 가까운 합의 절댓값" 저장
# 처음엔 아주 큰 값으로 설정
# inf는 무한대라는 뜻

a = 0
b = 0
# 정답이 될 두 용액 값을 저장할 변수

while left < right:
    # 같은 용액을 두 번 쓰면 안 되니까
    # left와 right가 겹치기 전까지만 반복

    mix = arr[left] + arr[right]
    # 현재 왼쪽 용액 + 오른쪽 용액을 섞은 값(합)

    if abs(mix) < best:
        # 지금 합의 절댓값이
        # 지금까지 저장한 값보다 더 0에 가깝다면

        best = abs(mix)
        # 더 좋은 답이니까 절댓값 갱신

        a = arr[left]
        b = arr[right]
        # 현재 두 용액 값을 정답 후보로 저장

    if mix < 0:
        # 합이 음수라는 건 너무 작은 값이라는 뜻
        # 예: -10, -3 같은 느낌
        # 0에 가까워지려면 합을 더 크게 만들어야 함

        left += 1
        # 왼쪽 포인터를 오른쪽으로 한 칸 이동
        # 정렬되어 있으므로 더 큰 값을 보게 됨
        # 그래서 합이 커질 가능성이 생김

    else:
        # 합이 0이거나 양수라는 뜻
        # 너무 크거나 이미 0인 상태

        right -= 1
        # 오른쪽 포인터를 왼쪽으로 한 칸 이동
        # 정렬되어 있으므로 더 작은 값을 보게 됨
        # 그래서 합이 작아질 가능성이 생김

print(a, b)
# 정답인 두 용액 값을 오름차순으로 출력
# 배열이 정렬되어 있고 left < right 상태에서 저장했기 때문에
# a가 b보다 작게 나옴